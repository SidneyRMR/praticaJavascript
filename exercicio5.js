function maiorOuIgual(num1, num2){
    if (num1 >= num2){ 
        return console.log(true)
    } else {
        return console.log(false)
    }
}


maiorOuIgual(2,4)
maiorOuIgual(2,2)
maiorOuIgual(2,1)


function igual(num1, num2){
    if (num1 === num2){ 
        return console.log(true)
    } else {
        return console.log(false)
    }
}

igual(2,4)
igual(2,2)
igual(2,1)